def suma(a=0, b=0):
    return a+b

def resta(a, b):
    return a-b

def multiplicacion(a, b):
    return a*b

def division(a, b):
    return a/b

print('La suma de los valores es: ', suma(12,3))
print('La resta de los valores es: ', resta(12,3))
print('La multiplicacion de los valores es: ', multiplicacion(12,3))
print('La divison de los valores es: ', division(12,3))